/**
 * -Encapsulamento:
 *      -Um objeto deve ser visto como uma cápsula:
 *      - Em seu interior: atributos
 *      - Em sua superficie: Métodos
 * -O que é um objeto?
 *      - Uma unidade que possi atributos, comportamento e se relaciona com os outros objetos por meio de mensagens;
 *      - Umm objeto pode ser concreto (Pessoa, um produto, um pedido) ou abstrato (uma conexão, uma janela, um arquivo xml)
 * -Classe:
 *      - uma classe é uma estrutura usada para criar objetos;
 *      - Uma classe é um modelo para criação de objetos;
 *      - Podem ser classes: entidades do negócio da aplicação (Pessoa, conta, cliente), calsses pra apresentação (janela,
 *      botão, frame, barra)..
 * -Atributos:
 *      -Um atributo é uma característica valorada, ou seja, é como um campo que contém uma informação relevante na classe;
 * -Metódo:
 *      -Os métodos representam ações que a classe irá realizar;
 *      -Definem como o objeto irá se comunicar com os outros;
 */